export type NoteTag = 'アイデア' | '情報' | 'ToDo'

export interface PackNote {
  title: string
  tag: NoteTag
  content: Record<string, unknown>  // Tiptap JSON
}

export interface ProfessionPack {
  id: string
  profession: string
  icon: string
  description: string
  color: string
  notes: PackNote[]
}

// ── ヘルパー ────────────────────────────────────────────────

function doc(...nodes: unknown[]) {
  return { type: 'doc', content: nodes }
}
function h2(text: string) {
  return { type: 'heading', attrs: { level: 2 }, content: [{ type: 'text', text }] }
}
function h3(text: string) {
  return { type: 'heading', attrs: { level: 3 }, content: [{ type: 'text', text }] }
}
function p(text: string) {
  return { type: 'paragraph', content: text ? [{ type: 'text', text }] : [] }
}
function ul(...items: string[]) {
  return {
    type: 'bulletList',
    content: items.map((text) => ({
      type: 'listItem',
      content: [{ type: 'paragraph', content: [{ type: 'text', text }] }],
    })),
  }
}
function callout(emoji: string, label: string) {
  return { type: 'callout', attrs: { emoji }, content: [p(label)] }
}
function taskList(...items: { text: string; done?: boolean }[]) {
  return {
    type: 'taskList',
    content: items.map(({ text, done = false }) => ({
      type: 'taskItem',
      attrs: { checked: done },
      content: [{ type: 'paragraph', content: [{ type: 'text', text }] }],
    })),
  }
}

